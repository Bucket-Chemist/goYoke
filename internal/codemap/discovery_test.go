// createFile writes content to path, creating parent dirs as needed.
func createFile(t *testing.T, path, content string) {
	t.Helper()
	require.NoError(t, os.MkdirAll(filepath.Dir(path), 0o755))
	require.NoError(t, os.WriteFile(path, []byte(content), 0o644))
}

// --- extractNameFromTOML ---

func TestExtractNameFromTOML(t *testing.T) {
	tests := []struct {
		name     string
		input    string
		expected string
	}{
		{"cargo style", "[package]\nname = \"my-crate\"\nversion = \"0.1.0\"\n", "my-crate"},
		{"pyproject style", "[tool.poetry]\nname = \"my-package\"\n", "my-package"},
		{"single quotes", "name = 'my-lib'\n", "my-lib"},
		{"no name field", "[package]\nversion = \"0.1.0\"\n", ""},
		{"empty input", "", ""},
		{"name with spaces around equals", "name  =  \"spaced\"\n", "spaced"},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			assert.Equal(t, tc.expected, extractNameFromTOML([]byte(tc.input)))
		})
	}
}

