const repoRoot = "../.."

func goKey(path string) ModuleKey { return ModuleKey{Path: path, Language: "go"} }

func TestDiscoverFiles_BasicGoProject(t *testing.T) {
	modules, err := DiscoverFiles(repoRoot, DiscoveryOpts{})
	require.NoError(t, err)
	assert.NotEmpty(t, modules, "expected at least one module in goYoke repo")

	// Verify well-known modules are present.
	_, hasValidate := modules[goKey("cmd/goyoke-validate")]
	_, hasArchive := modules[goKey("cmd/goyoke-archive")]
	assert.True(t, hasValidate, "expected cmd/goyoke-validate module")
	assert.True(t, hasArchive, "expected cmd/goyoke-archive module")

	// Every Go file should end in .go and not be a test file.
	for key, files := range modules {
		if key.Language != "go" {
			continue
		}
		for _, f := range files {
			assert.Equal(t, ".go", filepath.Ext(f), "module %v: non-.go file %s", key, f)
			assert.False(t, len(f) > 8 && f[len(f)-8:] == "_test.go",
				"module %v: test file %s should be excluded", key, f)
		}
	}
}

func TestDiscoverFiles_SkipsDefaultDirs(t *testing.T) {
	root := t.TempDir()
	createFile(t, filepath.Join(root, "vendor", "foo.go"), "package vendor\n")
	createFile(t, filepath.Join(root, "node_modules", "foo.go"), "package nm\n")
	createFile(t, filepath.Join(root, ".git", "foo.go"), "package git\n")
	createFile(t, filepath.Join(root, "testdata", "foo.go"), "package td\n")
	createFile(t, filepath.Join(root, "target", "foo.go"), "package target\n")
	createFile(t, filepath.Join(root, "real", "main.go"), "package real\n")

	modules, err := DiscoverFiles(root, DiscoveryOpts{})
	require.NoError(t, err)

	assert.Contains(t, modules, goKey("real"), "expected 'real' module")
	assert.NotContains(t, modules, goKey("vendor"))
	assert.NotContains(t, modules, goKey("node_modules"))
	assert.NotContains(t, modules, goKey(".git"))
	assert.NotContains(t, modules, goKey("testdata"))
	assert.NotContains(t, modules, goKey("target"))
}

