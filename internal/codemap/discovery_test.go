func TestModuleToFilename(t *testing.T) {
	tests := []struct {
		module   string
		expected string
	}{
		{"cmd/goyoke-validate", "cmd--goyoke-validate"},
		{"internal/codemap", "internal--codemap"},
		{"pkg/routing", "pkg--routing"},
		{"", "_root"},
		{"single", "single"},
		{"a/b/c", "a--b--c"},
	}
	for _, tc := range tests {
		t.Run(tc.module, func(t *testing.T) {
			assert.Equal(t, tc.expected, moduleToFilename(tc.module))
		})
	}
}

