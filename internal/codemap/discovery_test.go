	createFile(t, filepath.Join(root, "src", "main.go"), "package main\n")
	createFile(t, filepath.Join(root, "src", "lib.py"), "def hello(): pass\n")
	createFile(t, filepath.Join(root, "src", "app.ts"), "export const x = 1;\n")
