func TestDiscoverFiles_SpecificFiles(t *testing.T) {
	root := t.TempDir()
	a := filepath.Join(root, "pkg", "a.go")
	b := filepath.Join(root, "pkg", "b.go")
	c := filepath.Join(root, "pkg", "c.go")
	createFile(t, a, "package pkg\n")
	createFile(t, b, "package pkg\n")
	createFile(t, c, "package pkg\n")

	modules, err := DiscoverFiles(root, DiscoveryOpts{SpecificFiles: []string{a, b}})
	require.NoError(t, err)

	files := modules[goKey("pkg")]
	require.Len(t, files, 2)

	names := []string{filepath.Base(files[0]), filepath.Base(files[1])}
	assert.ElementsMatch(t, names, []string{"a.go", "b.go"})
}

func TestDiscoverFiles_GeneratedDetection(t *testing.T) {
	root := t.TempDir()
	genFile := filepath.Join(root, "pkg", "gen.go")
	realFile := filepath.Join(root, "pkg", "real.go")
	createFile(t, genFile, "// Code generated by protoc-gen-go. DO NOT EDIT.\npackage pkg\n")
	createFile(t, realFile, "package pkg\n")

	// Default: generated files excluded.
	modules, err := DiscoverFiles(root, DiscoveryOpts{})
	require.NoError(t, err)
	files := modules[goKey("pkg")]
	require.Len(t, files, 1)
	assert.Equal(t, "pkg/real.go", files[0])
}

